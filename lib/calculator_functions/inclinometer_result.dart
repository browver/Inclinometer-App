class InclinometerResult {
  final double depth;
  final double absoluteA;
  final double absoluteB;
  final double deviationA;
  final double deviationB;
  final String warning;

  InclinometerResult({
    required this.depth,
    required this.absoluteA,
    required this.absoluteB,
    required this.deviationA,
    required this.deviationB,
    required this.warning,
  });
}
